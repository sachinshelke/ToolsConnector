"""Generate the Go SDK surface from the SAME bindings as Python + TypeScript.

Emits two artifacts into ``go/``:

  * ``bindings_gen.go``        — every ConnectorBinding as embedded JSON, parsed
                                 into ``var ALL map[string]*ConnectorB`` at init.
  * ``cmd/parity/matrix.json`` — the parity MATRIX + PAGI as a fixture the Go
                                 parity harness replays.

The Go *runtime* (runtime.go) is hand-written once — it is the thin per-language
interpreter, the Go sibling of executor.py. Everything connector-specific is
generated from the declarative spec, exactly like the TS target.

Run:  .venv/bin/python -m experiments.sdk_spike.gen_go
"""

from __future__ import annotations

import json
import shutil
import subprocess
from pathlib import Path

from .binding_ir import Location, Style
from .parity import MATRIX, PAGI
from .specs import ALL

GO = Path(__file__).resolve().parent / "go"

# The connectors whose typed clients ship in the productized module.
CLIENTS = ["stripe", "github"]

_INITIALISMS = ("id", "url", "api", "sku", "uri")


def go_pascal(s: str) -> str:
    parts = []
    for w in s.split("_"):
        parts.append(w.upper() if w in _INITIALISMS else (w[:1].upper() + w[1:]))
    return "".join(parts)


def infer_ty(p) -> str:
    if p.ty:
        return p.ty
    if p.style in (Style.INDEXED, Style.BRACKET, Style.FORM_EXPLODE):
        return "string[]"
    if p.style == Style.INDEXED_OBJECT:
        return "smap[]"
    if p.style == Style.MAP:
        return "smap"
    if isinstance(p.default, bool):  # before int — bool is a subclass of int
        return "boolean"
    if p.max is not None or isinstance(p.default, int):
        return "number"
    return "string"


# abstract type -> (Go value type, is_scalar)
_GO_TY = {
    "string": ("string", True),
    "number": ("int", True),
    "boolean": ("bool", True),
    "string[]": ("[]string", False),
    "smap": ("map[string]string", False),
    "smap[]": ("[]map[string]string", False),
    "object": ("map[string]any", False),
    "object[]": ("[]map[string]any", False),
}


def emit_bindings() -> str:
    lines = [
        "// Code generated by gen_go.py from the connector bindings. DO NOT EDIT.",
        "package tcspike",
        "",
        "// ALL holds every connector binding, parsed once from the JSON the Python",
        "// IR emits. Same source of truth as the Python executor and TS runtime.",
        "var ALL = map[string]*ConnectorB{",
    ]
    for name, conn in ALL.items():
        # Compact JSON (model_dump_json) -> Go raw string literal. Bindings never
        # contain backticks, so a raw literal round-trips the JSON verbatim.
        js = conn.model_dump_json()
        assert "`" not in js, f"binding {name} contains a backtick; raw literal unsafe"
        lines.append(f'\t"{name}": MustParseBinding(`{js}`),')
    lines.append("}")
    lines.append("")
    return "\n".join(lines)


def emit_matrix() -> str:
    matrix = []
    for cname, (cred, actions) in MATRIX.items():
        for action, args in actions:
            matrix.append(
                {"connector": cname, "cred": cred, "action": action, "args": args}
            )
    pagi = [
        {
            "connector": c,
            "cred": cr,
            "action": a,
            "first_args": fa,
            "body": bd,
            "headers": hd,
        }
        for (c, cr, a, fa, bd, hd) in PAGI
    ]
    return json.dumps({"matrix": matrix, "pagi": pagi}, indent=1) + "\n"


def emit_client(name: str, package: str = "tcspike") -> str:
    """Emit a native, typed Go client for one connector — arg structs with a
    toMap() builder, delegating methods, paginate-all helpers, and escape-hatch
    delegation. Self-contained (carries its own binding)."""
    conn = ALL[name]
    C = go_pascal(conn.name)  # e.g. Stripe
    js = conn.model_dump_json()
    assert "`" not in js

    L = [
        "// Code generated by gen_go.py from the connector bindings. DO NOT EDIT.",
        f"package {package}",
        "",
        "import (",
        '\t"fmt"',
        '\t"net/http"',
        ")",
        "",
        f"// {conn.name.upper()} is the declarative binding driving every {C} request.",
        f"var {conn.name.upper()} = MustParseBinding(`{js}`)",
        "",
        f"// {C}Override implements an escape-hatch action that resists declarative binding.",
        f"type {C}Override func(cred string, args map[string]any) (any, error)",
        "",
        f"// {C} is an in-process, bring-your-own-key client. Every method builds a",
        "// request from the binding and issues it with the client's *http.Client.",
        f"type {C} struct {{",
        "\tcred      string",
        "\thttp      *http.Client",
        f"\toverrides map[string]{C}Override",
        "}",
        "",
        f"type {C}Option func(*{C})",
        "",
        f"// {C}WithHTTPClient overrides the *http.Client (e.g. to inject a test transport).",
        f"func {C}WithHTTPClient(c *http.Client) {C}Option {{ return func(s *{C}) {{ s.http = c }} }}",
        "",
        f"// {C}WithOverride registers a hand-written implementation for an escape-hatch action.",
        f"func {C}WithOverride(action string, fn {C}Override) {C}Option {{",
        f"\treturn func(s *{C}) {{ s.overrides[action] = fn }}",
        "}",
        "",
        f"// New{C} constructs a client for the given API key.",
        f"func New{C}(credential string, opts ...{C}Option) *{C} {{",
        f"\ts := &{C}{{cred: credential, http: http.DefaultClient, overrides: map[string]{C}Override{{}}}}",
        "\tfor _, o := range opts {",
        "\t\to(s)",
        "\t}",
        "\treturn s",
        "}",
        "",
        f"func (s *{C}) execute(action string, args map[string]any) (any, error) {{",
        f"\treturn ExecuteWith(s.http, {conn.name.upper()}, action, args, s.cred)",
        "}",
        "",
        f"func (s *{C}) paginate(action string, args map[string]any) ([]any, error) {{",
        f"\treturn PaginateWith(s.http, {conn.name.upper()}, action, args, s.cred)",
        "}",
        "",
    ]

    for a in conn.actions.values():
        args_ty = f"{go_pascal(a.name)}Args"
        # arg struct
        L.append(f"// {args_ty} are the arguments for {C}.{go_pascal(a.name)}.")
        L.append(f"type {args_ty} struct {{")
        field_lines = []
        set_lines = []
        for p in a.params:
            goty, scalar = _GO_TY[infer_ty(p)]
            # A path param is required only if it appears in the DEFAULT path;
            # path_variant-only params (GitHub org/user/workflow_id) are optional.
            in_default = p.location == Location.PATH and ("{" + p.wire + "}") in a.path
            required = in_default or p.required
            field = go_pascal(p.name)
            if required:
                field_lines.append(f"\t{field} {goty}")
                set_lines.append(f'\tm["{p.name}"] = a.{field}')
            elif scalar:
                field_lines.append(f"\t{field} *{goty}")
                set_lines.append(f"\tif a.{field} != nil {{ m[\"{p.name}\"] = *a.{field} }}")
            else:  # optional slice/map — nil means absent
                field_lines.append(f"\t{field} {goty}")
                set_lines.append(f"\tif a.{field} != nil {{ m[\"{p.name}\"] = a.{field} }}")
        L.extend(field_lines)
        L.append("}")
        L.append("")
        L.append(f"func (a {args_ty}) toMap() map[string]any {{")
        L.append("\tm := map[string]any{}")
        L.extend(set_lines)
        L.append("\treturn m")
        L.append("}")
        L.append("")
        # method
        L.append(f"// {go_pascal(a.name)} — {a.method} {a.path}")
        L.append(f"func (s *{C}) {go_pascal(a.name)}(args {args_ty}) (any, error) {{")
        L.append(f'\treturn s.execute("{a.name}", args.toMap())')
        L.append("}")
        L.append("")
        if a.pagination.kind.value != "none":
            L.append(f"// {go_pascal(a.name)}All walks every page of {a.name} and returns all items.")
            L.append(f"func (s *{C}) {go_pascal(a.name)}All(args {args_ty}) ([]any, error) {{")
            L.append(f'\treturn s.paginate("{a.name}", args.toMap())')
            L.append("}")
            L.append("")

    for name in conn.escape_hatches:
        m = go_pascal(name)
        L.append(f"// {m} is an ESCAPE-HATCH action: register an impl with {C}WithOverride(\"{name}\", fn).")
        L.append(f"func (s *{C}) {m}(args map[string]any) (any, error) {{")
        L.append(f'\tfn, ok := s.overrides["{name}"]')
        L.append("\tif !ok {")
        L.append(f'\t\treturn nil, fmt.Errorf("{conn.name}.{name} is an escape-hatch action; register an override")')
        L.append("\t}")
        L.append("\treturn fn(s.cred, args)")
        L.append("}")
        L.append("")

    return "\n".join(L)


def main() -> None:
    (GO / "cmd" / "parity").mkdir(parents=True, exist_ok=True)
    (GO / "bindings_gen.go").write_text(emit_bindings())
    (GO / "cmd" / "parity" / "matrix.json").write_text(emit_matrix())
    go_files = [GO / "bindings_gen.go"]
    for name in CLIENTS:
        f = GO / f"gen_{name}.go"
        f.write_text(emit_client(name))
        go_files.append(f)
    # Keep generated Go gofmt-clean so it passes the same bar as hand-written code.
    if gofmt := shutil.which("gofmt"):
        subprocess.run([gofmt, "-w", *map(str, go_files)], check=True)
    print("wrote:")
    print("    go/bindings_gen.go")
    print("    go/cmd/parity/matrix.json")
    for name in CLIENTS:
        print(f"    go/gen_{name}.go")


if __name__ == "__main__":
    main()
